package bai3;

public class thisinh {
    protected int SBD;
    protected String HoTen;
    protected String DiaChi;
    protected int MUT;
    public thisinh(int SBD, String HoTen, String DiaChi, int MUT)
    {
        this.SBD = SBD;
        this.HoTen = HoTen;
        this.DiaChi = DiaChi;
        this.MUT = MUT;
    }
    public int getSDB()
    {
        return SBD;
    }

}
